	<h1 id="uvg-slower">Clausura del Diplomado en Gestión Integrada de Agua</h1>

	<p class="page-notice" >
		El sábado 4 de septiembre de 2017 se llevó a cabo la Clausura del Diplomado en Gestión Integrada de Agua, en Universidad del Valle de Guatemala Campus Sur, Santa Lucía Cotzumalguapa. El diplomado se impartió del 10 de junio al 2 de septiembre con el apoyo del Proyecto “Conservación y Uso Sostenible de la Biodiversidad en Áreas Protegidas Marino Costeras” implementado por el Programa de las Naciones Unidas para el Desarrollo (PNUD), y ejecutado por el Ministerio de Ambiente y Recursos Naturales (MARN) y el Consejo Nacional de Áreas Protegidas (CONAP) con financiamiento del Fondo para el Medio Ambiente Mundial (GEF, por sus siglas en inglés). <br><br>
		Al diplomado asistieron 42 profesionales y miembros de la sociedad civil vinculados al tema del agua. Se contó con la participación de representantes de Gobernación Departamental, municipalidades de la costa sur, MARN, CONAP, Ministerio de Agricultura, Ganadería ya Alimentación (MAGA), Fondo de Tierras (FONTIERRAS),   Instituto de Ciencia y Tecnología (ICTA), Secretaría de Planificación y Programación de la Presidencia (Segeplán), Instituto Guatemalteco de Turismo (INGUAT), y las Industrias azucarera y, de palma africana.<br><br>
		Durante el diplomado la enseñanza fue teórica y práctica, dividida en 3 módulos:<br><br>
		Módulo I: Calidad del agua, caudales y cuencas hidrográficas.<br>
		Módulo II: Gestión comunitaria de recursos hídricos, normativos y planes de prevención y mitigación de desastres.<br>
		Módulo III: Economía, gobernanza y valoración del agua.<br><br>

		Este diplomado contribuye con el alcance del Objetivo de Desarrollo Sostenible 6 (ODS) para garantizar la disponibilidad de agua, su gestión sostenible y el saneamiento para todas y todos. También se vincula con el ODS 14 sobre Vida Marina ya que las acciones humanas que se desarrollan cuenca arriba tienen impacto sobre los mares y océanos.<br><br>
		Próximos Pasos:<br><br>
		Se espera que los participantes pongan en práctica los conocimientos adquiridos en el diplomado a través de monitorear fuentes de agua, tomar muestras e interpretarlas por medio de análisis de laboratorio. Desarrollando procesos de gestión integrada de los recursos hídricos, redactando informes y elaborando costos de manejo y valoración de los recursos, fortaleciendo la gestión del agua y brindando un mejor bienestar a sus comunidades.


