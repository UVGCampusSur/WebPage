				<div class="page-notice less-padding">
					<h2><strong>Patrocinio de la Universidad</strong></h2> 
					La Fundación de la Universidad del Valle de Guatemala es una organización no lucrativa y con personería jurídica que patrocina a la Universidad.
					<br><br>
					<h2><strong>Consejo de Fiduciarios</strong></h2>
					La Fundación de la Universidad del Valle de Guatemala fija los principios básicos que
					rigen el desarrollo de la Universidad y delega su ejecución en el Consejo de
					Fiduciarios de la Fundación.
					<br><br>
					<h2><strong>Consejo Directivo</strong></h2>
					La organización administrativa y académica de la Universidad está bajo la
					responsabilidad de su Consejo Directivo presidido por el Rector. El Consejo Directivo
					está integrado por los siguientes miembros: Rector, Vicerrector, Secretario, Director
					de Estudios, Secretario Ejecutivo de la Fundación de la Universidad del Valle de
					Guatemala y Asesores.
					<br><br>
					<h2><strong>Consejo Académico</strong></h2>
					El Consejo Académico de la Universidad es un organismo integrado por los miembros
					del Consejo Directivo, los Decanos, y los Directores de los Departamentos. Sus
					atribuciones están estrechamente relacionadas con la planificación y promoción de las
					actividades docentes y de investigación.
					<br><br>
					<h2><strong>Estructura Académica</strong></h2>
					La Universidad del Valle de Guatemala Campus Sur está constituida por la Facultad
					de Ingeniería y Facultad de Educación.
					<br><br>
					<h2><strong>Títulos y Grados</strong></h2>

					La Universidad otorga el título de profesor de enseñanza media en diversas
					disciplinas, profesor en diversas especializaciones, Profesor de educación primaria,
					Profesor especializado en problemas de aprendizaje, Profesor epecializado en
					Educación física y Deportes. Así mismo, otorga los grados de Técnico Universitario,
					Licenciatura y Maestría.<br><br>
					Para obtener el Profesorado se requieren tres años de estudio como mínimo; a
					excepción del Profesorado especializado en inglés donde se requieren cuatro años de
					estudio. Al cerrar el profesorado se necesitan 2 años más de estudio para cerrar la
					licenciatura. Se requieres 2 años de estudio para cerrar un Técnico universitario y
					luego 3 años más para cerrar la Licenciatura en Ingeniería en cualquiera de las
					especialidades y la maestría de dos años.
					<br><br>
					<h2><strong>Año Académico</strong></h2>
					El año académico se divide en dos ciclos:
						<ul class="orden">
							<li>Primer Ciclo, de Enero a Junio</li>
							<li>Segundo Ciclo, de Julio a fines de Noviembre</li>
						</ul>
					<br>
					Las fechas en que se inicia y termina cada ciclo las fija el calendario que se publica
					anualmente; las clases comienzan, generalmente, en la cuarta semana de enero y
					concluyen en la última semana de noviembre.
					<br><br>
					<h2><strong>Horarios</strong></h2>
					Las actividades académicas se desarrollan entre las 7:00 y las 21:00 horas, de lunes
					a viernes, y los sábados de sábado de 7:00 a 17:00 horas.
					<br><br>
					<h2><strong>Períodos de clase y actividades de laboratorio y de campo</strong></h2>
					Los períodos de clase duran regularmente 45 minutos, pero el tiempo puede variar
					según la naturaleza del curso y las características especiales del programa. El número
					de periodos por semana para clases teóricas oscila entre dos y cuatro; para las clases
					de ciencias que incluyen prácticas de laboratorio, oscila entre cinco y seis.
					El tiempo para las actividades de campo y los trabajos de investigación es variable y
					depende de la naturaleza y características especiales de unos y otros.
					<br><br>
					<h2><strong>Créditos académicos</strong></h2>
					La Universidad otorga un crédito en el ciclo por cada periodo semanal de clase de 45 minutos.

					<h2><strong>Estudiantes</strong></h2>
					El programa de trabajo de la Universidad está concebido para el estudio y la
					investigación de carácter serio y riguroso; la institución selecciona a sus estudiantes
					con base en su capacidad y responsabilidad y exige de ellos un alto rendimiento y la
					comprensión de sus propósitos educativos.<br><br>
					Los estudiantes están obligados a:<br><br>
					<ul class="orden">
						<li>Cumplir con los estatutos, reglamentos, disposiciones e instructivos de la Universidad.</li>
						<li>Cumplir con los requisitos y exigencias de los estudios.</li>
						<li>Comportarse con corrección y disciplina, manteniendo en todo momento la conducta y actitudes decorosas inherentes a su condición de miembros de la Universidad.</li>
						<li>Coadyuvar con la Universidad en el cuidado de los bienes de la misma y de todas las pertenencias al servicio de la institución.</li>
					</ul><br>
					La Universidad se reserva el derecho de negar reinscripción en cualquier ciclo a los
					estudiantes cuyo rendimiento y actitud hacia el estudio sean deficientes. Así mismo,
					se reserva el derecho de retirar a los estudiantes cuyas actitudes o conducta sean
					incompatibles con el ambiente universitario o constituyan falta de respeto hacia los
					demás.
					<br><br>
					<h2><strong>Honores y distinciones</strong></h2>
					La Universidad otorga distinciones anuales a los estudiantes que por su asistencia, dedicación y rendimiento académico superior obtengan la calidad de alumnos distinguidos, a juicio del Consejo Directivo de la Universidad.
					<br><br>
					A las personas que hayan sido objeto de la distinción mencionada en el punto anterior
					durante la mayoría de los años de su carrera y que presenten un trabajo de
					graduación de calidad excelente, la Universidad les otorga Cum Laude en el diploma y
					en el expediente de graduación.<br><br>

					<h2><strong>Organizaciones estudiantiles</strong></h2>
					Los estudiantes pueden organizar agrupaciones con fines universitarios, siempre que
					las actividades correspondientes no interfieran con las labores académicas y
					docentes. No son aceptables aquellas actividades que tiendan al proselitismo político
					o religioso, ni las que sean superfluas o ajenas a los fines de la Universidad, según
					criterio del Consejo Directivo.

					<h2><strong>Requisitos de ingreso</strong></h2>
					Para ingresar a la Universidad del Valle de Guatemala Campus Sur como estudiante
					regular se requiere:
					<ul class="orden">
						<li>Haber completado estudios de educación media y poseer el diploma correspondiente</li>
						<li>Realizar la prueba diagnóstica en el calendario programado.</li>
					</ul><br>
				

