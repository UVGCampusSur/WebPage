					<h2 style="text-align:center;"><strong>Historia de la Universidad del Valle de Guatemala Campus Sur</strong></h2>
					<br>

					En 1995, con el apoyo de la Industria Azucarera se abre el segundo campus de la
					Universidad del Valle de Guatemala, el programa Educativo del Sur (Hoy UVG
					Campus Sur). El Grupo Educativo del Valle abre sus puertas aperturando el
					Colegio Americano del Sur, en las instalaciones del Club social Madre Tierra con
					51 estudiantes.<br><br>
					Poco tiempo después de su fundación, en 1997 la Universidad del Valle de
					Guatemala Campus Sur inicia la Maestría en Ingeniería cañera con 57 estudiantes
					y la maestría en Ingeniería Azucarera con 39 estudiantes. Este año también se
					apertura un programa más “Educación Continua EDUCON”.<br><br>
					En 1998 se concluye la primera fase de construcción de las modernas
					instalaciones, ese mismo año se traslada el Colegio Americano del Sur a la nueva
					ubicación del campus. Este año abre sus puertas la Facultad de Educación con 20
					estudiantes del Profesorado Especializado en Educación Primaria, también se
					apertura el Centro de Idiomas, ofreciendo cursos de inglés a los padres de familia
					del Colegio Americano del Sur.<br><br>
					Para continuar con el objetivo de apoyar a estudiantes talentosos de la región, en
					el año 2000 se apertura el Instituto Tecnológico, impartiendo bachillerato en
					Ciencias y Letras con diversas especialidades.<br><br>
					Para ampliar las oportunidades de desarrollo profesional en el año 2002 se
					apertura las carreras técnicas universitarias vinculadas al área agrícola e
					industrial, con el transcurrir de los años surge la necesidad de aperturar otras
					carreras y es en el 2009 cuando inician las ingenierías y la licenciatura en
					educación, como seguimiento a las carreras técnicas y profesorados.<br><br>
					La Universidad del Valle de Guatemala Campus Sur, se fundó para descentralizar
					las oportunidades de desarrollo, satisfacer las necesidades e intereses de los
					profesionales de la costa sur, mejorar la educación, fortalecer el crecimiento y
					desarrollo social, económico y cultural de la región.<br><br>
					Actualmente operan el Colegio Americano del Sur, el Centro de Idiomas, el
					Instituto Tecnológico (Bachilleratos),la Facultad de Ingeniería con dos programas
					de licenciatura y la Maestría en Producción y Gestión Avícola y Pecuaria, y la
					Facultad de Educación con varios profesorados y la licenciatura en Educación.<br><br>
