	<h1 id="uvg-slower">UVG CAMPUS SUR Fomentando el Emprendimiento en Escuintla</h1>

	<p class="page-notice" >
		El día miércoles 4 de octubre en las instalaciones del Instituto Experimental Carlos Samayoa Chinchilla de Escuintla se realizó el Taller el Valor del Emprendimiento con la participación de más de 100 jóvenes que han culminado el 3º. Básico en dicho establecimiento.<br><br>

		El objetivo de la actividad era incentivar a los jóvenes a acercarse a las instalaciones de la Universidad del Valle de Guatemala campus Sur y poder inscribirse en algunas de las opciones que se ofrecen en la carrera de Bachillerato.<br><br>

		En el Taller se analizaron y reflexionaron varios videos sobre Nunca Rendirse y como poder sacar lo mejor de cada joven emprendedor, asimismo se tuvo la participación de 3 jóvenes destacados de ITEC que compartieron su testimonio y experiencia como becados de AZASGUA.<br><br>
		
		Este evento fue coordinado y facilitado conjuntamente por la Licda. Tomasita Henricx, Directora ITEC UVG Sur y el Lic. Luis Urizar, Coordinador de Innovación y Emprendimiento de UVG sedes externas.<br><br>
		
		Los jóvenes participaron en la dinámica Torre de papel donde se trabajaron los siguientes objetivos: Trabajo en equipo, planeación estratégica, liderazgo, comunicación.  Para explicar se explicó a los participantes las carreras que tiene la UVG en campus Sur y se les entrego material promocional.<br><br>


	</p>
