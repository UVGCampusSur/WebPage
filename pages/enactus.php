<h1>El equipo UVG Campus Sur clasifica a la final de la competencia nacional Enactus</h1>

<p class="page-notice" >
	El 11 de julio de 2017 se llevó a cabo la jornada de eliminatoria de la copa nacional Enactus Guatemala en  Sede de Academics, el equipo Enactus UVG Campus Sur participo en la liga pantaleon junto a la Universidad Francisco Marroquín y CUNOC resultando ganador de liga el equipo de estudiantes que represento a UVG Sur. <br><br>
	La competencia final se realizará el martes 18 de julio en el hotel Intercontinental, con la participación de 3 equipos finalistas: Enactus Universidad Mariano Gálvez, Enactus CUNOC y Enactus UVG Campus Sur. El equipo ganador representará a Guatemala en la competencia mundial Enactus en Londres Inglaterra.<br><br>
	Es importante recordar que UVG Campus Sur ha sido campeón en 3 copas nacionales. En el año 2013 gano la competencia nacional y represento a Guatemala en Cancún México,  en el año 2014 represento a Guatemala en Beijing China, y en el 2015 represento a Guatemala en Johannesburgo Sudáfrica.

</p>
