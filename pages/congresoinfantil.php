	<p class="page-notice" >
		Por tercer año consecutivo Ingenio Magdalena y  UVG Campus Sur, celebran el Congreso Infantil de Liderazgo, con el tema “Construyo mis sueños para un mundo mejor”, dirigido a 100 estudiantes de EORM de aldea Tierra Nueva y los representantes de los gobiernos escolares de 15 establecimientos beneficiados por el Programa en Valores.<br><br>
		Este se llevó a cabo el día miércoles 06 de septiembre en instalaciones de UVG, Campus Sur con el propósito de motivar a los niños y niñas participantes a tener una actitud positiva frente a la vida y a luchar por sus sueños por medio de experiencias significativas en las que practiquen habilidades, destrezas, actitudes y valores, que todo líder debe poseer.<br><br>
		Las actividades planificadas fueron de carácter educativo y centradas en los valores trabajados por el Programa Integral de Formación en Valores durante en el presente año:<br><br>
		Liderazgo y prudencia. <br>
		Emprendimiento y proactividad.<br>
		Creatividad e innovación.<br>
		Esfuerzo y constancia.<br><br>

		Con esta base el “III Congreso Infantil de liderazgo” busca provocar el despertar de nuevos líderes, inspirados en la construcción de una mejor comunidad y una mejor sociedad a través del propio legado.<br><br>

