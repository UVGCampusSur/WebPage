	<h1 id="uvg-slower">UVG fomentando Innovación en Campus Externos</h1>

	<p class="page-notice" >
		El martes 30 de enero se tuvo la visita del Ing. Ian Charnas en las instalaciones de
		UVG campus Sur, donde realizó un recorrido a los principales sitios de interés
		incluyendo el laboratorio Makerspace, la Biblioteca, los laboratorios de Ciencias y
		posteriormente se realizó un Taller con personal Directivo del campus, una
		delegación de UVG campus central y los Administradores de los centros de
		Innovación de los 3 campus.
		<br><br>
		Ian Charnas es el gerente de Think [box] el espacio abierto universitario más
		grande del mundo ubicado en Western Reserve University es gratuito y público.
		Ian es Ingeniero Mecánico e Ingeniero en Sistemas. Su trabajo personal combina
		arte y tecnología en proyectos grupales de temática creativa.
		<br><br>
		Ian ha viajado por el mundo presentando sus inventos y se le conoce como el
		maestro de los makerspace y ha realizado proyectos muy exitosos e innovadores.
		Esta visita fue muy importante porque la UVG en campus Central, Sur y Altiplano
		tiene previsto para el 2018 el funcionamiento de sus espacios de innovación, que
		estarán al servicio de estudiantes como de jóvenes emprendedores de las
		comunidades vinculadas a nuestra Universidad.
		<br><br>
		Los Makerspaces son espacios colaborativos para crear, aprender, explorar y
		compartir que utilizan herramientas manuales y de alta tecnología. Son espacios
		abiertos a personas creativas y emprendedoras.
		<br><br>
		En el Taller realizado el martes 30 se discutieron los siguientes temas: Políticas
		de seguridad, mantenimiento de equipo, entrenamiento de personal, comunicación
		y señalización, manejo de inventario, manejo de voluntarios y casos de éxito en
		los makerspace.
		<br>


